export class Calculate {
  Add(number1, number2) {
    return number1 + number2
  }
  Subtract(number1, number2){
    return number1 - number2
  }
  Multiply(number1, number2){
    return number1 * number2
  }
  Divide(number1, number2){
    return number1 / number2
  }
  Exponent(number1, number2){
    return number1 ** number2;
  }
  Percent(number1, number2){
    return number2 / 100;
  }
}
